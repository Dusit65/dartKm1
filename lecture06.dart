void main(){
  // Operator in Dart
  // + ,- ,* , / , % , ++ , -- (Arithmetic)
  // ==, !=, >, <, >=, <=      (Comparison)
  // &&, ||, !                 (Logical)
  // ? :                       (Ternary)

  //condition ? do when true : do when false
  int aa = 100;
  print(aa < 99 ? "Hello" : "Bye");

  int bb = 10 * aa < 1000 ? 20 : 5;
  print(bb);
}
